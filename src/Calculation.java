public class Calculation {
    public static void main(String[] args) {
        System.out.println("5+5는 "+(5+5)+"입니다.");
        System.out.println("5-5는 "+(5-5)+"입니다.");
        System.out.println("5x5는 "+(5*5)+"입니다.");
        System.out.println("5/5는 "+(5/5)+"입니다.");
        System.out.println("5%3의 나머지는 "+(5%3)+"입니다.");
    }
}
