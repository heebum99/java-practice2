public class Type {
    public static void main(String[] args) {
        // 정수/정수 -> 정수
        System.out.println("3/2 = "+(3/2)); 
        
        //실수를 포함해 계산 -> 실수
        System.out.println("3.0/2.0 = "+(3.0/2.0));
        System.out.println("3.0/2 = "+(3.0/2));
        System.out.println("3/2.0 = "+(3/2.0));
    }
}
